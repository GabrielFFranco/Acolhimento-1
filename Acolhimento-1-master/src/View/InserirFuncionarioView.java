    private void bSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSalvarActionPerformed
        
        Funcionario funcionario = new Funcionario();
        
        try {
            funcionario.setNome(txtNome.getText());
            funcionario.setCpf(txtCpf.getText());

            String datan = txtDataNasc.getText();
            SimpleDateFormat form = new SimpleDateFormat("dd/mm/YYYY");
            java.util.Date dutiln;
            dutiln = form.parse(datan);
            
            funcionario.setDataNascimento(dutiln);
            
            funcionario.setEmail(txtEmail.getText());
            funcionario.setEndereco(txtEndereco.getText());
            funcionario.setTelefone(txtTelefone.getText());
            funcionario.setRg(txtRg.getText());
            int status = cbStatus.getSelectedIndex();
            if(status == 0){
                funcionario.setStatus(Status.ATIVO);
            }else if(status == 1){
                funcionario.setStatus(Status.INATIVO);
            }
            int tipo = cbTipo.getSelectedIndex();
            switch (tipo) {
                case 0:
                    funcionario.setTipo(Tipo.AUXILIAR);
                    break;
                case 1:
                    funcionario.setTipo(Tipo.COORDENADOR);
                    break;
                case 2:
                    funcionario.setTipo(Tipo.TECNICOs);
                    break;
                default:
                    break;
            }
            
        } catch (ParseException ex) {
            ex.printStackTrace();
        }
        
        
        FuncionarioDao.Salvar(funcionario);
    }//GEN-LAST:event_bSalvarActionPerformed

